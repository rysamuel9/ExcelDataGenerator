class Employee:
    def __init__(self, employee_id, email, name, role, phone, address, dob, city, gender, status,
                 salary, contract_type, hr_pic, join_date):
        self.employee_id = employee_id
        self.email = email
        self.name = name
        self.role = role
        self.phone = phone
        self.address = address
        self.dob = dob
        self.city = city
        self.gender = gender
        self.status = status
        self.salary = salary
        self.contract_type = contract_type
        self.hr_pic = hr_pic
        self.join_date = join_date